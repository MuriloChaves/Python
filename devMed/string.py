#coding: utf-8

# String
string = 'Murilo Chaves'
print('Nome: %s' % string)

# Inteiro
inteiro = 42
print('A resposta da vida, do universo e tudo mais é: %d' % inteiro)

# Real - ponto flutuante
pi = 3.14159265
print('O dia oficial do π foi: %.4f (entendedores entenderão)' % pi)

# Octal
octal = 1992
print('O ano que nasci em octal é: %o' % octal)

# Hexadecimal
hexadecimnal = 23
print('Meu número preferido em hexadecimal é: %x' % hexadecimnal)

# Exponencial
exponencial = 25/7
print('Exponencial: %e' % exponencial)

# Porcentagem
porcentagem = 50
print('Porcentagem: %d%%' % porcentagem)