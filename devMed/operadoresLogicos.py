#coding: utf-8

# Maior
print(5 > 6)

# Maior ou Igual
print(6 >= 6)

# Menor ou igual
print(4 <= 5)

# Igual
print(5 == 5)

# Diferente
print(0 != 1)

# Operador NÃO - not
print(not True)

# Operador E - and
print(0 and 1)

# Operador OU - or
print(0 or 1)

# Contido
print(5 in [1, 2, 3])

# Não Contém
print(5 not in [1, 2, 3])

# Relação
print(5 is 5)